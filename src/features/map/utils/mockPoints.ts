export function toGeoJSON(points: VinPoint[]) {
  return {
    type: "FeatureCollection" as const,
    features: points.map((point) => ({
      type: "Feature" as const,
      geometry: {
        type: "Point" as const,
        coordinates: [point.lon, point.lat],
      },
      properties: {
        vin: point.vin,
        make: point.make,
        model: point.model,
        year: point.year,
        fuelType: point.fuelType,
        recallFlag: point.recallFlag,
      },
    })),
  };
}
